import Foundation

class RedheadDuck: BaseDuck {
    override func quack() {
        print("Redhead quack")
    }
    override func display() {
        print("Redhead display")
    }
    override func swim() {
        print("Redhead swim")
    }
    override func fly() {
        print("Redhead fly")
    }
}
